# Manipulação de tipos de dados em Python

# Convertendo tipos
numero_str = "123"  # string
numero_int = int(numero_str)  # converte para inteiro
print(f"O número como inteiro é: {numero_int}")

# Convertendo float para inteiro
numero_float = 45.67
numero_int = int(numero_float)  # converte para inteiro
print(f"O número float convertido para inteiro é: {numero_int}")

